// employee constructor
class Employee {
  constructor(name, id, email) {
    this.name = name;
    this.id = id;
    this.email = email;
  }

  // returning name from input
  getName() {
    return this.name;
  }

  // returning ID from input
  getName() {
    return this.id;
  }

  // returning email from input
  getName() {
    return this.email;
  }

  // returning employee type
  getName() {
    return this.email;
  }
}

// module to be exported
module.exports = Employee;
